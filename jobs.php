    <script type="text/javascript">
        (function() {
            // handle bullet of the slide counter
            $('#slideshow-controls .bullets div').first().addClass('current')
            var nextSlideIndex = 1,
                    countMainSlides = $('#slideshow .slide').length,
                    intervalId
            $('#slideshow-controls .bullets div').click(function() {
                var idx = $(this).index()
                showSlideAtIndex(idx)
                resetSlideshow()
            })
            if (countMainSlides > 1) intervalId = setInterval(function() {
                showSlideAtIndex(nextSlideIndex)
                nextSlideIndex = (++nextSlideIndex) % countMainSlides
            }, 5000)

          function resetSlideshow() {
            clearInterval(intervalId)
            intervalId = setInterval(function() {
                showSlideAtIndex(nextSlideIndex)
                nextSlideIndex = (++nextSlideIndex) % countMainSlides
            }, 5000)
         }
         function showSlideAtIndex(desiredIndex) {
            var _$current = $('#slideshow .slide:last')
            $('#slideshow .slide[slidx="' + desiredIndex + '"]').insertBefore(_$current).show()
            _$current.fadeOut('slow', function() {
                $(this).prependTo('#slideshow')
            })
            $('#slideshow-controls .bullets .current').removeClass('current')
            $('#slideshow-controls .bullets div').eq(desiredIndex).addClass('current')
         }
         $('div#jobs .job h3 span.colapsed').click(function() {
            $('div#jobs .job ul').hide()
            $('div#jobs .job h3 span.colapsed').removeClass('disclosed').addClass('colapsed')
            $(this).removeClass('colapsed').addClass('disclosed')
            $(this).parents('.job').find('ul').slideDown()
         })
         
            $('nav li.home.has-sub').addClass('current')
            $('nav li.jobs').addClass('current')
        })()
    </script>
